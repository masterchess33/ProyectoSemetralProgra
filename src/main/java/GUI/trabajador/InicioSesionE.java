    private void iniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_iniciarSesionActionPerformed
        Archivos j= new Archivos();
        j.leerE();
        
        int posicion = -1;
        String nombre=jTextField1.getText();
        String contra=jTextField2.getText();
        try{
        for (int i = 0; i < Coleccion.getTrab().size(); i++) {
            if (nombre.equals(Coleccion.getTrab().get(i).getNombreCuenta())) {
                if (Coleccion.getTrab().get(i).getContrasenia().equals(contra)) {
                    
                    posicion = i;
                    break;
                }
            }
        }
        if (posicion == -1) {
            jTextArea1.setText("Error, nombre o contrase침a incorrecta");
        }else{
            MenuEmpleado n=new MenuEmpleado();
            n.setNumeroCuenta(posicion);
            n.setLocationRelativeTo(null);
            n.setVisible(true);
            this.dispose();
        }
        }catch(IndexOutOfBoundsException e){
            jTextArea1.setText("Es probable que no existan cuentas registradas");
        }
    }//GEN-LAST:event_iniciarSesionActionPerformed

